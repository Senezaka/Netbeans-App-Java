    private void update_button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_button1ActionPerformed
    try{
        Brg b = new Brg();
        this.stat = k.getCon().prepareStatement("update barang set nama_barang=?, "+"harga=?, status=? where id_barang=?");
        stat.setString(1, b.nama_barang);
        stat.setInt(2,b.harga);
        stat.setString(3,b.status);
        stat.setInt(4, Integer.parseInt(idbarang_field.getText()));
        stat.executeUpdate();
        refreshtable();
    }catch (Exception e){
        JOptionPane.showMessageDialog(null, e.getMessage());
}                                            
  
    }//GEN-LAST:event_update_button1ActionPerformed

