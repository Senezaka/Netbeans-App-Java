    class Brg extends Barang{
        int id_barang, harga;
        String nama_barang, status;
        
        public Brg(){
            this.nama_barang = namabarang_field.getText();
            this.harga = Integer.parseInt(harga_field.getText());
            this.status = status_combo.getSelectedItem().toString();
        }
    }
    
    public void refreshtable(){
        model = new DefaultTableModel();
        model.addColumn("ID Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga");
        model.addColumn("Status Barang");

        table_barang.setModel(model);

        try{
            this.stat = k.getCon().prepareStatement("Select * from barang");
            this.rs = this.stat.executeQuery();
            while(rs.next()){
                Object[] data = {
                    rs.getInt("id_barang"),
                    rs.getString("nama_barang"),
                    rs.getInt("harga"),
                    rs.getString("status")
            };
            model.addRow(data);
        }
    }catch (Exception e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }   
    idbarang_field.setText("");
    namabarang_field.setText("");
    harga_field.setText("");
    }
