    private void update_button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_button3ActionPerformed
    try{
            transaksi tran = new transaksi();
            tran.id_transaksi = Integer.parseInt(idtransaksi_field.getText());
            this.stat = k.getCon().prepareStatement("update transaksi set nama_pelanggan=?,"
            + "id_barang=?,tanggal =?,nama_barang=?,harga=?,jumlah_beli=?,total_bayar=? "
            + "where id_transaksi=?");
            this.stat.setString(1, tran.nama_pelanggan);
            this.stat.setInt(2, tran.id_barang);
            this.stat.setString(3, tran.tanggal);
            this.stat.setString(4, tran.nama_barang);
            this.stat.setInt(5, tran.harga);
            this.stat.setInt(6, tran.jumlah_beli);
            this.stat.setInt(7, tran.total_bayar);
            this.stat.setInt(8, tran.id_transaksi);
            this.stat.executeUpdate();
            refreshTable();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_update_button3ActionPerformed

    private void delete_button3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_button3ActionPerformed
    try{
        transaksi tran = new transaksi();
        tran.id_transaksi = Integer.parseInt(idtransaksi_field.getText());
        this.stat = k.getCon().prepareStatement("delete from transaksi " + "where id_transaksi=?");
        this.stat.setInt(1, tran.id_transaksi);
        this.stat.executeUpdate();
        refreshTable();
    }catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_delete_button3ActionPerformed

